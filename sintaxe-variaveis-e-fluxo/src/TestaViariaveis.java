
public class TestaViariaveis {
	
	public static void main(String[] args) {
		System.out.println("Explorando variáveis");
		
		int idade;
		idade = 28;
		
		System.out.println(idade);
		
		idade = 30 + 10;
		System.out.println(idade);
		
		idade = 7 * (5 + 2);
		System.out.println(idade);
		
		System.out.println("A idade é " + idade + ", parabéns!");
	}

}
